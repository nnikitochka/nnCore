package me.nnikitochka.nncore.blockData.events;

import org.bukkit.block.Block;
import org.bukkit.event.Event;
import org.bukkit.plugin.Plugin;
import org.jetbrains.annotations.NotNull;

public class BlockDataMoveEvent extends BlockDataEvent {

    private final @NotNull Block blockTo;

    public BlockDataMoveEvent(@NotNull Plugin plugin, @NotNull Block blockFrom, @NotNull Block blockTo, @NotNull Event bukkitEvent) {
        super(plugin, blockFrom, bukkitEvent);
        this.blockTo = blockTo;
    }

    public @NotNull Block getBlockTo() {
        return blockTo;
    }

}
