package me.nnikitochka.nncore.commands.utils;

import me.nnikitochka.nncore.commands.MessageCommand;
import org.bukkit.entity.Player;

import java.util.List;

public class CommandsPerform {
    public void perform(Player player, List<String> commands) {
        for (String command: commands)
            perform(player, command);
    }

    public void perform(Player player, String command) {
        String[] args = command.split(" ");
        String withoutCMD = command.replace(args[0]+" ", "");

        if (CommandsUtils.commands.containsKey(args[0])) {
            CommandsUtils.commands.get(args[0]).perform(player, args, withoutCMD);
        } else {
            new MessageCommand().perform(player, args, args[0]);
        }
    }
}
